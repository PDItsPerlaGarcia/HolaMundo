public class Primitivos {
    public static void main(String[] args) {
        byte numByte = 21;
        System.out.println("numByte = " + numByte);
        System.out.println("Tipo byte en byte es: "+Byte.BYTES);
        System.out.println("Tipo byte en bits es: "+Byte.SIZE);
        System.out.println("Tamaño máximo de byte: "+Byte.MAX_VALUE);
        System.out.println("Tamañño minimo de byte: "+Byte.MIN_VALUE);

        short numShort = 160;
        System.out.println("numShort = " + numShort);
        System.out.println("Tipo short en byte es: "+Short.BYTES);
        System.out.println("Tipo short en bits es: "+Short.SIZE);
        System.out.println("Tamaño máximo de short: "+Short.MAX_VALUE);
        System.out.println("Tamañño minimo de short: "+Short.MIN_VALUE);

        int numInt = 4032;
        System.out.println("numInt = " + numInt);
        System.out.println("Tipo int en byte es: "+Integer.BYTES);
        System.out.println("Tipo int en bits es: "+Integer.SIZE);
        System.out.println("Tamaño máximo de int: "+Integer.MAX_VALUE);
        System.out.println("Tamañño minimo de int: "+Integer.MIN_VALUE);

        long numLong = 4032945L;
        System.out.println("numLong = " + numLong);
        System.out.println("Tipo long en byte es: "+Long.BYTES);
        System.out.println("Tipo long en bits es: "+Long.SIZE);
        System.out.println("Tamaño máximo de long: "+Long.MAX_VALUE);
        System.out.println("Tamañño minimo de long: "+Long.MIN_VALUE);

        float numFloat = 124.34f;
        System.out.println("numFloat = " + numFloat);
        System.out.println("Tipo float en byte es: "+Float.BYTES);
        System.out.println("Tipo float en bits es: "+Float.SIZE);
        System.out.println("Tamaño máximo de float: "+Float.MAX_VALUE);
        System.out.println("Tamañño minimo de float: "+Float.MIN_VALUE);

        double numDouble = 12453.254D;
        System.out.println("numDouble = " + numDouble);
        System.out.println("Tipo double en byte es: "+Double.BYTES);
        System.out.println("Tipo double en bits es: "+Double.SIZE);
        System.out.println("Tamaño máximo de double: "+Double.MAX_VALUE);
        System.out.println("Tamañño minimo de double: "+Double.MIN_VALUE);

    }
}
